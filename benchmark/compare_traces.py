#!/usr/bin/env python3
"""
Trace Kernel Diff Tool

Compares two trace analysis Excel files (generated by trace_module_analyzer.py) to
identify kernel replacements, additions, and removals between ROCm/SGLang image versions.

Strategy:
  1. Parse per-module-type detail sheets from both files
  2. Match sheets by (module_type, phase) key
  3. Diff kernel name sequences using SequenceMatcher
  4. Group results by (module_type, phase); report diffs
  5. Output terminal report + optional Excel diff report

Usage:
    python compare_traces.py file_a.xlsx file_b.xlsx [--output diff_report.xlsx] [--verbose]
"""

import argparse
import collections
import difflib
import re
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple

import openpyxl
from openpyxl.styles import Font, PatternFill
from openpyxl.utils import get_column_letter


# ── Constants ────────────────────────────────────────────────────────────────

LENGTH_RATIO_THRESHOLD = 3.0

# Sheets that are NOT per-module-type detail tabs
_KNOWN_SHEETS = {"Summary", "Overview", "Module Tree", "GPU Kernels", "Model Info (WIP)"}


# ── Data structures ──────────────────────────────────────────────────────────

@dataclass
class KernelInfo:
    short_name: str      # kernel name (short)
    kernel_name: str     # full kernel name
    category: str        # kernel category
    duration_us: float = 0.0
    module_instance: str = ""
    input_dims: str = ""


@dataclass
class ModuleInfo:
    """Represents one detail sheet (one module_type + phase combination)."""
    module_type: str
    phase: str           # "pre", "dec", or ""
    kernels: List[KernelInfo]
    kernel_count: int = 0
    kernel_sum_us: float = 0.0
    wall_time_us: float = 0.0

    @property
    def short_name_tuple(self) -> Tuple[str, ...]:
        return tuple(k.short_name for k in self.kernels)

    @property
    def type_phase(self) -> Tuple[str, str]:
        return (self.module_type, self.phase)

    # Aliases for backward compatibility with trace_diff.py
    @property
    def layer_type(self) -> str:
        return self.module_type

    @property
    def stage(self) -> str:
        return self.phase

    @property
    def layer_idx(self) -> int:
        return 0  # Not applicable for module-based analysis

    @property
    def type_stage(self) -> Tuple[str, str]:
        return self.type_phase


@dataclass
class DiffEntry:
    pos: int
    file_a_short: Optional[str]
    file_a_kernel: Optional[str]
    file_a_duration: Optional[float]
    file_b_short: Optional[str]
    file_b_kernel: Optional[str]
    file_b_duration: Optional[float]
    status: str  # SAME, REPLACED, ADDED, REMOVED


@dataclass
class PairResult:
    """Result of comparing one module pair."""
    layer_a: ModuleInfo
    layer_b: ModuleInfo
    status: str  # IDENTICAL, CHANGED, LENGTH_MISMATCH
    entries: List[DiffEntry] = field(default_factory=list)
    similarity: float = 0.0
    match_method: str = "type_phase"


@dataclass
class GroupResult:
    """Result for one (module_type, phase) group."""
    layer_type: str     # module_type
    stage: str          # phase
    status: str         # IDENTICAL, CHANGED, LENGTH_MISMATCH, ONLY_IN_A, ONLY_IN_B
    n_pairs: int = 0
    n_identical: int = 0
    n_changed: int = 0
    n_skipped: int = 0
    representative: Optional[PairResult] = None
    only_layers: List[ModuleInfo] = field(default_factory=list)
    skipped_pairs: List[PairResult] = field(default_factory=list)

    @property
    def change_summary(self) -> str:
        if self.status == "IDENTICAL":
            return "-"
        if self.status in ("ONLY_IN_A", "ONLY_IN_B"):
            n = len(self.only_layers)
            return f"({n} module{'s' if n != 1 else ''})"
        if self.status == "LENGTH_MISMATCH":
            return f"all {self.n_skipped} pair{'s' if self.n_skipped != 1 else ''} skipped"
        if not self.representative:
            return "-"
        counts = collections.Counter(
            e.status for e in self.representative.entries if e.status != "SAME"
        )
        parts = []
        for s in ("REPLACED", "ADDED", "REMOVED"):
            if counts[s]:
                parts.append(f"{counts[s]} {s.lower()}")
        return ", ".join(parts) or "-"


# ── Step 1: Parse Excel ─────────────────────────────────────────────────────

def parse_excel(filepath: str, verbose: bool = False) -> Dict[Tuple[str, str], ModuleInfo]:
    """Parse a trace_module_analyzer Excel file and return modules indexed by (type, phase)."""
    wb = openpyxl.load_workbook(filepath, read_only=True)
    modules: Dict[Tuple[str, str], ModuleInfo] = {}

    for sheet_name in wb.sheetnames:
        if sheet_name in _KNOWN_SHEETS:
            continue

        ws = wb[sheet_name]
        rows = list(ws.iter_rows(values_only=True))
        if not rows:
            continue

        # Parse sheet name for module_type and phase
        phase_match = re.search(r'\((\w+)\)\s*$', sheet_name)
        if phase_match:
            phase = phase_match.group(1)
            module_type = sheet_name[:phase_match.start()].strip()
        else:
            phase = ""
            module_type = sheet_name.strip()

        # Row 1: title — "Name [phase] — N kernels"
        title_str = str(rows[0][0]) if rows[0][0] else ""
        kernel_count_match = re.search(r'(\d+)\s+kernels?', title_str)
        kernel_count = int(kernel_count_match.group(1)) if kernel_count_match else 0

        # Row 2: "Kernel sum: X us | Wall time: Y us | Overlap: Z us"
        kernel_sum_us = 0.0
        wall_time_us = 0.0
        if len(rows) > 1 and rows[1][0]:
            info_str = str(rows[1][0])
            sum_match = re.search(r'Kernel sum:\s*([\d,.]+)\s*us', info_str)
            wall_match = re.search(r'Wall time:\s*([\d,.]+)\s*us', info_str)
            if sum_match:
                kernel_sum_us = float(sum_match.group(1).replace(",", ""))
            if wall_match:
                wall_time_us = float(wall_match.group(1).replace(",", ""))

        # Find kernel detail section
        kernels = []
        in_kernels = False

        for row in rows[2:]:
            cell0 = str(row[0]).strip() if row[0] is not None else ""

            # Detect kernel detail header
            if cell0 == "Module" and len(row) > 2:
                cell2 = str(row[2]).strip() if row[2] is not None else ""
                if cell2 == "Kernel Name":
                    in_kernels = True
                    continue

            if in_kernels:
                if not cell0 or cell0.startswith("... truncated"):
                    if cell0.startswith("... truncated"):
                        break
                    continue
                try:
                    module_inst = cell0
                    input_dims = str(row[1]) if len(row) > 1 and row[1] is not None else ""
                    kernel_name = str(row[2]) if len(row) > 2 and row[2] is not None else ""
                    duration = float(row[3]) if len(row) > 3 and row[3] is not None else 0.0
                    category = str(row[5]) if len(row) > 5 and row[5] is not None else ""
                    # Use kernel name as short_name (no separate short name in new format)
                    kernels.append(KernelInfo(
                        short_name=kernel_name,
                        kernel_name=kernel_name,
                        category=category,
                        duration_us=duration,
                        module_instance=module_inst,
                        input_dims=input_dims,
                    ))
                except (ValueError, TypeError):
                    continue

        key = (module_type, phase)
        modules[key] = ModuleInfo(
            module_type=module_type,
            phase=phase,
            kernels=kernels,
            kernel_count=kernel_count,
            kernel_sum_us=kernel_sum_us,
            wall_time_us=wall_time_us,
        )

    wb.close()
    return modules


# ── Step 2: Match modules ────────────────────────────────────────────────────

def match_layers(
    modules_a: Dict[Tuple[str, str], ModuleInfo],
    modules_b: Dict[Tuple[str, str], ModuleInfo],
    verbose: bool = False,
) -> Tuple[List[Tuple[ModuleInfo, ModuleInfo, str]], List[ModuleInfo], List[ModuleInfo]]:
    """Match modules between two files by (module_type, phase) key.

    Returns: (matched_pairs, only_in_a, only_in_b)
    """
    matched = []
    only_in_a = []
    only_in_b = []

    all_keys = sorted(set(modules_a.keys()) | set(modules_b.keys()))

    for key in all_keys:
        in_a = key in modules_a
        in_b = key in modules_b

        if in_a and in_b:
            matched.append((modules_a[key], modules_b[key], "type_phase"))
        elif in_a:
            only_in_a.append(modules_a[key])
        else:
            only_in_b.append(modules_b[key])

    if verbose:
        print(f"  Matching: {len(matched)} matched, "
              f"{len(only_in_a)} only-in-A, {len(only_in_b)} only-in-B")

    return matched, only_in_a, only_in_b


# ── Step 3: Per-pair diff ───────────────────────────────────────────────────

def diff_pair(a: ModuleInfo, b: ModuleInfo, match_method: str) -> PairResult:
    """Compute diff for one module pair."""
    len_a = len(a.kernels)
    len_b = len(b.kernels)
    ratio = max(len_a, len_b) / max(min(len_a, len_b), 1)

    if ratio > LENGTH_RATIO_THRESHOLD:
        return PairResult(a, b, "LENGTH_MISMATCH", similarity=0.0,
                          match_method=match_method)

    if a.short_name_tuple == b.short_name_tuple:
        return PairResult(a, b, "IDENTICAL", similarity=1.0,
                          match_method=match_method)

    entries = _diff_kernel_sequences(a, b)
    n_same = sum(1 for e in entries if e.status == "SAME")
    similarity = n_same / len(entries) if entries else 0.0

    return PairResult(a, b, "CHANGED", entries=entries, similarity=similarity,
                      match_method=match_method)


def _diff_kernel_sequences(a: ModuleInfo, b: ModuleInfo) -> List[DiffEntry]:
    """Build a diff table from two modules using SequenceMatcher."""
    seq_a = list(a.short_name_tuple)
    seq_b = list(b.short_name_tuple)
    infos_a = a.kernels
    infos_b = b.kernels

    matcher = difflib.SequenceMatcher(None, seq_a, seq_b)
    entries = []
    pos = 0

    for tag, i1, i2, j1, j2 in matcher.get_opcodes():
        if tag == "equal":
            for ai, bi in zip(range(i1, i2), range(j1, j2)):
                pos += 1
                entries.append(DiffEntry(
                    pos=pos,
                    file_a_short=seq_a[ai],
                    file_a_kernel=infos_a[ai].kernel_name,
                    file_a_duration=infos_a[ai].duration_us,
                    file_b_short=seq_b[bi],
                    file_b_kernel=infos_b[bi].kernel_name,
                    file_b_duration=infos_b[bi].duration_us,
                    status="SAME",
                ))
        elif tag == "replace":
            la = i2 - i1
            lb = j2 - j1
            for k in range(max(la, lb)):
                pos += 1
                a_idx = i1 + k if k < la else None
                b_idx = j1 + k if k < lb else None
                if a_idx is not None and b_idx is not None:
                    entries.append(DiffEntry(
                        pos=pos,
                        file_a_short=seq_a[a_idx],
                        file_a_kernel=infos_a[a_idx].kernel_name,
                        file_a_duration=infos_a[a_idx].duration_us,
                        file_b_short=seq_b[b_idx],
                        file_b_kernel=infos_b[b_idx].kernel_name,
                        file_b_duration=infos_b[b_idx].duration_us,
                        status="REPLACED",
                    ))
                elif a_idx is not None:
                    entries.append(DiffEntry(
                        pos=pos,
                        file_a_short=seq_a[a_idx],
                        file_a_kernel=infos_a[a_idx].kernel_name,
                        file_a_duration=infos_a[a_idx].duration_us,
                        file_b_short=None, file_b_kernel=None,
                        file_b_duration=None,
                        status="REMOVED",
                    ))
                else:
                    entries.append(DiffEntry(
                        pos=pos,
                        file_a_short=None, file_a_kernel=None,
                        file_a_duration=None,
                        file_b_short=seq_b[b_idx],
                        file_b_kernel=infos_b[b_idx].kernel_name,
                        file_b_duration=infos_b[b_idx].duration_us,
                        status="ADDED",
                    ))
        elif tag == "insert":
            for bi in range(j1, j2):
                pos += 1
                entries.append(DiffEntry(
                    pos=pos,
                    file_a_short=None, file_a_kernel=None,
                    file_a_duration=None,
                    file_b_short=seq_b[bi],
                    file_b_kernel=infos_b[bi].kernel_name,
                    file_b_duration=infos_b[bi].duration_us,
                    status="ADDED",
                ))
        elif tag == "delete":
            for ai in range(i1, i2):
                pos += 1
                entries.append(DiffEntry(
                    pos=pos,
                    file_a_short=seq_a[ai],
                    file_a_kernel=infos_a[ai].kernel_name,
                    file_a_duration=infos_a[ai].duration_us,
                    file_b_short=None, file_b_kernel=None,
                    file_b_duration=None,
                    status="REMOVED",
                ))

    return entries


# ── Step 4: Group results & select representative ───────────────────────────

def group_and_select(
    pair_results: List[PairResult],
    only_in_a: List[ModuleInfo],
    only_in_b: List[ModuleInfo],
    verbose: bool = False,
) -> List[GroupResult]:
    """Group per-pair results by (module_type, phase) and pick representative."""
    # Each pair is already unique by (type, phase), so groups are 1:1
    # But we keep the same structure for API compatibility
    groups: Dict[Tuple[str, str], List[PairResult]] = {}
    for pr in pair_results:
        key = pr.layer_a.type_phase
        groups.setdefault(key, []).append(pr)

    only_a_groups: Dict[Tuple[str, str], List[ModuleInfo]] = {}
    only_b_groups: Dict[Tuple[str, str], List[ModuleInfo]] = {}
    for module in only_in_a:
        only_a_groups.setdefault(module.type_phase, []).append(module)
    for module in only_in_b:
        only_b_groups.setdefault(module.type_phase, []).append(module)

    all_keys = sorted(
        set(groups.keys()) | set(only_a_groups.keys()) | set(only_b_groups.keys())
    )

    results = []
    for key in all_keys:
        module_type, phase = key
        pairs = groups.get(key, [])
        oa = only_a_groups.get(key, [])
        ob = only_b_groups.get(key, [])

        if not pairs and not oa and not ob:
            continue

        if not pairs:
            if oa and not ob:
                results.append(GroupResult(module_type, phase, "ONLY_IN_A",
                                          only_layers=oa))
            elif ob and not oa:
                results.append(GroupResult(module_type, phase, "ONLY_IN_B",
                                          only_layers=ob))
            else:
                results.append(GroupResult(module_type, phase, "ONLY_IN_A",
                                          only_layers=oa))
                results.append(GroupResult(module_type, phase, "ONLY_IN_B",
                                          only_layers=ob))
            continue

        identical = [p for p in pairs if p.status == "IDENTICAL"]
        changed = [p for p in pairs if p.status == "CHANGED"]
        skipped = [p for p in pairs if p.status == "LENGTH_MISMATCH"]

        n_pairs = len(pairs)
        n_identical = len(identical)
        n_changed = len(changed)
        n_skipped = len(skipped)

        valid = identical + changed

        if not valid:
            gr = GroupResult(module_type, phase, "LENGTH_MISMATCH",
                             n_pairs=n_pairs, n_skipped=n_skipped,
                             skipped_pairs=skipped)
            gr.only_layers = oa + ob
            results.append(gr)
            continue

        if not changed:
            gr = GroupResult(module_type, phase, "IDENTICAL",
                             n_pairs=n_pairs, n_identical=n_identical,
                             n_skipped=n_skipped, skipped_pairs=skipped)
            gr.only_layers = oa + ob
            results.append(gr)
            continue

        representative = max(changed, key=lambda p: p.similarity)

        if verbose:
            print(f"  ({module_type}, {phase}): {n_pairs} pairs — "
                  f"{n_identical} identical, {n_changed} changed, {n_skipped} skipped")

        gr = GroupResult(module_type, phase, "CHANGED",
                         n_pairs=n_pairs, n_identical=n_identical,
                         n_changed=n_changed, n_skipped=n_skipped,
                         representative=representative,
                         skipped_pairs=skipped)
        gr.only_layers = oa + ob
        results.append(gr)

    return results


# ── Step 5: Terminal output ──────────────────────────────────────────────────

def _truncate(s: Optional[str], width: int) -> str:
    if s is None:
        return "-"
    return s if len(s) <= width else s[: width - 1] + "…"


def output_terminal_report(
    groups: List[GroupResult], file_a: str, file_b: str, verbose: bool = False
) -> None:
    """Print the diff report to the terminal."""
    sep = "=" * 70
    print()
    print(sep)
    print("  Trace Kernel Diff Report")
    print(f"  File A: {file_a}")
    print(f"  File B: {file_b}")
    print(sep)

    print()
    print("--- Module Type Summary ---")
    col_mt = max(20, max((len(g.layer_type) for g in groups), default=10))
    col_ph = max(8, max((len(g.stage) for g in groups), default=5))
    hdr = (f"  {'Module Type':<{col_mt}} | {'Phase':<{col_ph}} | "
           f"{'Status':<15} | {'Pairs':>5} | {'Skip':>4} | Changes")
    print(hdr)
    print(f"  {'-' * col_mt}-+-{'-' * col_ph}-+-{'-' * 15}-+-{'-' * 5}-+-"
          f"{'-' * 4}-+-{'-' * 30}")

    for g in groups:
        pairs_str = str(g.n_pairs) if g.n_pairs else "-"
        skip_str = str(g.n_skipped) if g.n_skipped else "-"
        if g.status in ("ONLY_IN_A", "ONLY_IN_B"):
            pairs_str = "-"
            skip_str = "-"
        print(f"  {g.layer_type:<{col_mt}} | {g.stage:<{col_ph}} | "
              f"{g.status:<15} | {pairs_str:>5} | {skip_str:>4} | {g.change_summary}")

    for g in groups:
        if g.status == "IDENTICAL":
            continue

        print()
        print(f"--- {g.layer_type} ({g.stage}) — {g.status} ---")

        if g.status in ("ONLY_IN_A", "ONLY_IN_B"):
            label = "A" if g.status == "ONLY_IN_A" else "B"
            modules = g.only_layers
            print(f"  Present only in File {label} ({len(modules)} module{'s' if len(modules) != 1 else ''})")
            if modules:
                first = modules[0]
                print(f"  Kernel sequence ({len(first.kernels)} kernels):")
                for i, k in enumerate(first.kernels[:20], 1):
                    print(f"    {i:3d}. {k.short_name}")
                if len(first.kernels) > 20:
                    print(f"    ... ({len(first.kernels) - 20} more)")
            continue

        if g.status == "LENGTH_MISMATCH":
            print(f"  All {g.n_skipped} pair{'s' if g.n_skipped != 1 else ''} had kernel count "
                  f"ratio > {LENGTH_RATIO_THRESHOLD:.0f}x — skipped")
            for p in g.skipped_pairs:
                la, lb = len(p.layer_a.kernels), len(p.layer_b.kernels)
                ratio = max(la, lb) / max(min(la, lb), 1)
                print(f"    {p.layer_a.module_type} ({la} kernels) <-> "
                      f"{p.layer_b.module_type} ({lb} kernels) — {ratio:.1f}x")
            continue

        rep = g.representative
        print(f"  {g.n_pairs} pairs: {g.n_identical} identical, "
              f"{g.n_changed} changed, {g.n_skipped} skipped")
        print(f"  Representative: {rep.layer_a.module_type} ({rep.layer_a.phase}) "
              f"(similarity: {rep.similarity:.1%}, match: {rep.match_method})")
        print()

        col_w = 25
        dur_w = 14
        hdr_a = f"{'File A (old)':<{col_w}}"
        hdr_a_dur = f"{'A Dur (us)':>{dur_w}}"
        hdr_b = f"{'File B (new)':<{col_w}}"
        hdr_b_dur = f"{'B Dur (us)':>{dur_w}}"
        print(f"  {'#':>3} | {hdr_a} | {hdr_a_dur} | {hdr_b} | {hdr_b_dur} | Status")
        print(f"  {'-' * 3}-+-{'-' * col_w}-+-{'-' * dur_w}-+-{'-' * col_w}-+-{'-' * dur_w}-+-{'-' * 10}")
        for e in rep.entries:
            a_str = _truncate(e.file_a_short, col_w)
            a_dur = f"{e.file_a_duration:>{dur_w}.3f}" if e.file_a_duration is not None else f"{'-':>{dur_w}}"
            b_str = _truncate(e.file_b_short, col_w)
            b_dur = f"{e.file_b_duration:>{dur_w}.3f}" if e.file_b_duration is not None else f"{'-':>{dur_w}}"
            print(f"  {e.pos:3d} | {a_str:<{col_w}} | {a_dur} | {b_str:<{col_w}} | {b_dur} | {e.status}")

        if verbose and rep.entries:
            changed_entries = [e for e in rep.entries if e.status != "SAME"]
            if changed_entries:
                print()
                print("  Full kernel names for changed entries:")
                for e in changed_entries:
                    print(f"    #{e.pos} [{e.status}]")
                    if e.file_a_kernel:
                        print(f"      A: {e.file_a_kernel}")
                    if e.file_b_kernel:
                        print(f"      B: {e.file_b_kernel}")

    print()


# ── Step 6: Excel output ────────────────────────────────────────────────────

_FILL_GREEN = PatternFill(start_color="C6EFCE", end_color="C6EFCE", fill_type="solid")
_FILL_YELLOW = PatternFill(start_color="FFEB9C", end_color="FFEB9C", fill_type="solid")
_FILL_RED = PatternFill(start_color="FFC7CE", end_color="FFC7CE", fill_type="solid")
_FILL_BLUE = PatternFill(start_color="BDD7EE", end_color="BDD7EE", fill_type="solid")
_FILL_GRAY = PatternFill(start_color="D9D9D9", end_color="D9D9D9", fill_type="solid")
_FILL_HEADER = PatternFill(start_color="D9E2F3", end_color="D9E2F3", fill_type="solid")
_FONT_BOLD = Font(bold=True)
_FONT_TITLE = Font(bold=True, size=12)

_STATUS_FILLS = {
    "SAME": _FILL_GREEN,
    "REPLACED": _FILL_YELLOW,
    "REMOVED": _FILL_RED,
    "ADDED": _FILL_BLUE,
}

_GROUP_STATUS_FILLS = {
    "IDENTICAL": _FILL_GREEN,
    "CHANGED": _FILL_YELLOW,
    "ONLY_IN_A": _FILL_RED,
    "ONLY_IN_B": _FILL_BLUE,
    "LENGTH_MISMATCH": _FILL_GRAY,
}


def output_excel_report(
    groups: List[GroupResult], file_a: str, file_b: str, output_path: str
) -> None:
    """Write a color-coded Excel diff report."""
    wb = openpyxl.Workbook()

    ws = wb.active
    ws.title = "Summary"

    ws.append(["Trace Kernel Diff Report"])
    ws["A1"].font = _FONT_TITLE
    ws.append([f"File A: {file_a}"])
    ws.append([f"File B: {file_b}"])
    ws.append([])

    headers = ["Module Type", "Phase", "Status", "Pairs", "Skipped", "Changes"]
    ws.append(headers)
    for col_idx in range(1, len(headers) + 1):
        ws.cell(row=ws.max_row, column=col_idx).font = _FONT_BOLD
        ws.cell(row=ws.max_row, column=col_idx).fill = _FILL_HEADER

    for g in groups:
        pairs = g.n_pairs if g.n_pairs else "-"
        skipped = g.n_skipped if g.n_skipped else "-"
        if g.status in ("ONLY_IN_A", "ONLY_IN_B"):
            pairs = "-"
            skipped = "-"
        ws.append([g.layer_type, g.stage, g.status, pairs, skipped, g.change_summary])
        fill = _GROUP_STATUS_FILLS.get(g.status)
        if fill:
            ws.cell(row=ws.max_row, column=3).fill = fill

    _auto_width(ws)

    for g in groups:
        if g.status == "IDENTICAL":
            continue

        raw_name = f"{g.layer_type} ({g.stage})" if g.stage else g.layer_type
        sheet_name = raw_name[:31]
        existing = {s.title for s in wb.worksheets}
        if sheet_name in existing:
            sheet_name = sheet_name[:28] + "_2"
        ws = wb.create_sheet(title=sheet_name)

        ws.append([f"{g.layer_type} ({g.stage}) — {g.status}"])
        ws["A1"].font = _FONT_TITLE

        if g.status in ("ONLY_IN_A", "ONLY_IN_B"):
            label = "A" if g.status == "ONLY_IN_A" else "B"
            ws.append([f"Present only in File {label} ({len(g.only_layers)} modules)"])
            ws.append([])
            if g.only_layers:
                first = g.only_layers[0]
                detail_headers = ["#", "Kernel Name", "Category", "Duration (us)"]
                ws.append(detail_headers)
                for ci in range(1, len(detail_headers) + 1):
                    ws.cell(row=ws.max_row, column=ci).font = _FONT_BOLD
                    ws.cell(row=ws.max_row, column=ci).fill = _FILL_HEADER
                for i, ki in enumerate(first.kernels, 1):
                    ws.append([i, ki.short_name, ki.category,
                               round(ki.duration_us, 3)])
            _auto_width(ws)
            continue

        if g.status == "LENGTH_MISMATCH":
            ws.append([f"All {g.n_skipped} pairs skipped (kernel count ratio > "
                       f"{LENGTH_RATIO_THRESHOLD:.0f}x)"])
            _auto_width(ws)
            continue

        rep = g.representative
        ws.append([f"{g.n_pairs} pairs: {g.n_identical} identical, "
                   f"{g.n_changed} changed, {g.n_skipped} skipped"])
        ws.append([f"Representative: {rep.layer_a.module_type} ({rep.layer_a.phase}) "
                   f"(similarity: {rep.similarity:.1%}, match: {rep.match_method})"])
        ws.append([])

        detail_headers = ["Pos", "FileA_KernelName",
                          "FileA_Duration (us)",
                          "FileB_KernelName",
                          "FileB_Duration (us)", "Status"]
        ws.append(detail_headers)
        header_row = ws.max_row
        for ci in range(1, len(detail_headers) + 1):
            ws.cell(row=header_row, column=ci).font = _FONT_BOLD
            ws.cell(row=header_row, column=ci).fill = _FILL_HEADER

        for e in rep.entries:
            ws.append([
                e.pos,
                e.file_a_kernel or "-",
                round(e.file_a_duration, 3) if e.file_a_duration is not None else "-",
                e.file_b_kernel or "-",
                round(e.file_b_duration, 3) if e.file_b_duration is not None else "-",
                e.status,
            ])
            fill = _STATUS_FILLS.get(e.status)
            if fill:
                for ci in range(1, len(detail_headers) + 1):
                    ws.cell(row=ws.max_row, column=ci).fill = fill

        _auto_width(ws)

    wb.save(output_path)
    print(f"Excel report saved to: {output_path}")


def _auto_width(ws, max_width: int = 50) -> None:
    """Auto-adjust column widths based on content."""
    for col_cells in ws.columns:
        max_len = 0
        col_letter = get_column_letter(col_cells[0].column)
        for cell in col_cells:
            if cell.value is not None:
                max_len = max(max_len, len(str(cell.value)))
        ws.column_dimensions[col_letter].width = min(max_len + 2, max_width)


# ── Main ────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Compare two trace analysis Excel files to identify kernel changes.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""\
Examples:
  python compare_traces.py old_analysis.xlsx new_analysis.xlsx
  python compare_traces.py old.xlsx new.xlsx --output diff_report.xlsx --verbose
""",
    )
    parser.add_argument("file_a", help="First (old) trace analysis Excel file")
    parser.add_argument("file_b", help="Second (new) trace analysis Excel file")
    parser.add_argument("--output", "-o", help="Output Excel diff report path")
    parser.add_argument("--verbose", "-v", action="store_true", help="Show detailed output")
    args = parser.parse_args()

    # Step 1: Parse
    print(f"Parsing File A: {args.file_a}")
    modules_a = parse_excel(args.file_a, verbose=args.verbose)
    print(f"  Found {len(modules_a)} module types")

    print(f"Parsing File B: {args.file_b}")
    modules_b = parse_excel(args.file_b, verbose=args.verbose)
    print(f"  Found {len(modules_b)} module types")

    # Step 2: Match
    print("\nMatching module types...")
    matched, only_a, only_b = match_layers(modules_a, modules_b, verbose=args.verbose)
    print(f"  {len(matched)} pairs matched, "
          f"{len(only_a)} only-in-A, {len(only_b)} only-in-B")

    # Step 3: Per-pair diff
    print("Computing per-pair diffs...")
    pair_results = []
    for a, b, method in matched:
        pr = diff_pair(a, b, method)
        pair_results.append(pr)

    n_id = sum(1 for p in pair_results if p.status == "IDENTICAL")
    n_ch = sum(1 for p in pair_results if p.status == "CHANGED")
    n_sk = sum(1 for p in pair_results if p.status == "LENGTH_MISMATCH")
    print(f"  {n_id} identical, {n_ch} changed, {n_sk} skipped (length mismatch)")

    # Step 4: Group
    if args.verbose:
        print("\nSelecting representatives...")
    group_results = group_and_select(pair_results, only_a, only_b, verbose=args.verbose)

    # Step 5: Terminal report
    output_terminal_report(group_results, args.file_a, args.file_b, verbose=args.verbose)

    # Step 6: Excel report (optional)
    if args.output:
        output_excel_report(group_results, args.file_a, args.file_b, args.output)


if __name__ == "__main__":
    main()
