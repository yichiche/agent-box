def output_excel_report(
    groups: List[GroupResult], file_a: str, file_b: str, output_path: str
) -> None:
    """Write a color-coded Excel diff report."""
    wb = openpyxl.Workbook()

    # ── Summary sheet ──
    ws = wb.active
    ws.title = "Summary"

    ws.append(["Trace Kernel Diff Report"])
    ws["A1"].font = _FONT_TITLE
    ws.append([f"File A: {file_a}"])
    ws.append([f"File B: {file_b}"])
    ws.append([])

    headers = ["Layer Type", "Stage", "Status", "Pairs", "Skipped", "Changes"]
    ws.append(headers)
    for col_idx in range(1, len(headers) + 1):
        ws.cell(row=ws.max_row, column=col_idx).font = _FONT_BOLD
        ws.cell(row=ws.max_row, column=col_idx).fill = _FILL_HEADER

    for g in groups:
        pairs = g.n_pairs if g.n_pairs else "-"
        skipped = g.n_skipped if g.n_skipped else "-"
        if g.status in ("ONLY_IN_A", "ONLY_IN_B"):
            pairs = "-"
            skipped = "-"
        ws.append([g.layer_type, g.stage, g.status, pairs, skipped, g.change_summary])

        fill = _GROUP_STATUS_FILLS.get(g.status)
        if fill:
            ws.cell(row=ws.max_row, column=3).fill = fill

    _auto_width(ws)

    # ── Per group detail sheets ──
    for g in groups:
        if g.status == "IDENTICAL":
            continue

        raw_name = f"{g.layer_type} ({g.stage})"
        sheet_name = raw_name[:31]
        existing = {s.title for s in wb.worksheets}
        if sheet_name in existing:
            sheet_name = sheet_name[:28] + "_2"
        ws = wb.create_sheet(title=sheet_name)

        ws.append([f"{g.layer_type} ({g.stage}) — {g.status}"])
        ws["A1"].font = _FONT_TITLE

        if g.status in ("ONLY_IN_A", "ONLY_IN_B"):
            label = "A" if g.status == "ONLY_IN_A" else "B"
            ws.append([f"Present only in File {label} ({len(g.only_layers)} layers)"])
            ws.append([])

            if g.only_layers:
                first = min(g.only_layers, key=lambda l: l.layer_idx)
                detail_headers = ["#", "Short Name", "Kernel Name", "Type"]
                ws.append(detail_headers)
                for ci in range(1, len(detail_headers) + 1):
                    ws.cell(row=ws.max_row, column=ci).font = _FONT_BOLD
                    ws.cell(row=ws.max_row, column=ci).fill = _FILL_HEADER
                for i, ki in enumerate(first.kernels, 1):
                    ws.append([i, ki.short_name, ki.kernel_name, ki.kernel_type])
            _auto_width(ws)
            continue

        if g.status == "LENGTH_MISMATCH":
            ws.append([f"All {g.n_skipped} pairs skipped (kernel count ratio > "
                       f"{LENGTH_RATIO_THRESHOLD:.0f}x)"])
            ws.append([])
            for p in g.skipped_pairs:
                la, lb = len(p.layer_a.kernels), len(p.layer_b.kernels)
                ratio = max(la, lb) / max(min(la, lb), 1)
                ws.append([f"Layer_{p.layer_a.layer_idx} ({la} kernels) <-> "
                           f"Layer_{p.layer_b.layer_idx} ({lb} kernels) — {ratio:.1f}x"])
            _auto_width(ws)
            continue

        # CHANGED
        rep = g.representative
        ws.append([f"{g.n_pairs} pairs: {g.n_identical} identical, "
                   f"{g.n_changed} changed, {g.n_skipped} skipped"])
        ws.append([f"Representative: Layer_{rep.layer_a.layer_idx} <-> "
                   f"Layer_{rep.layer_b.layer_idx} "
                   f"(similarity: {rep.similarity:.1%}, match: {rep.match_method})"])
        ws.append([])

        detail_headers = ["Pos", "FileA_ShortName", "FileA_KernelName",
                          "FileB_ShortName", "FileB_KernelName", "Status"]
        ws.append(detail_headers)
        header_row = ws.max_row
        for ci in range(1, len(detail_headers) + 1):
            ws.cell(row=header_row, column=ci).font = _FONT_BOLD
            ws.cell(row=header_row, column=ci).fill = _FILL_HEADER

        for e in rep.entries:
            ws.append([
                e.pos,
                e.file_a_short or "-",
                e.file_a_kernel or "-",
                e.file_b_short or "-",
                e.file_b_kernel or "-",
                e.status,
            ])
            fill = _STATUS_FILLS.get(e.status)
            if fill:
                for ci in range(1, len(detail_headers) + 1):
                    ws.cell(row=ws.max_row, column=ci).fill = fill

        _auto_width(ws)

    wb.save(output_path)
    print(f"Excel report saved to: {output_path}")


def _auto_width(ws, max_width: int = 50) -> None:
    """Auto-adjust column widths based on content."""
    for col_cells in ws.columns:
        max_len = 0
        col_letter = get_column_letter(col_cells[0].column)
        for cell in col_cells:
            if cell.value is not None:
                max_len = max(max_len, len(str(cell.value)))
        ws.column_dimensions[col_letter].width = min(max_len + 2, max_width)


# ── Main ────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Compare two trace analysis Excel files to identify kernel changes.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""\
Examples:
  python compare_traces.py old_analysis.xlsx new_analysis.xlsx
  python compare_traces.py old.xlsx new.xlsx --output diff_report.xlsx --verbose
""",
    )
    parser.add_argument("file_a", help="First (old) trace analysis Excel file")
    parser.add_argument("file_b", help="Second (new) trace analysis Excel file")
    parser.add_argument("--output", "-o", help="Output Excel diff report path")
    parser.add_argument("--verbose", "-v", action="store_true", help="Show detailed output")
    args = parser.parse_args()

    # Step 1: Parse
    print(f"Parsing File A: {args.file_a}")
    layers_a = parse_excel(args.file_a, verbose=args.verbose)
    print(f"  Found {len(layers_a)} layers")

    print(f"Parsing File B: {args.file_b}")
    layers_b = parse_excel(args.file_b, verbose=args.verbose)
    print(f"  Found {len(layers_b)} layers")

    # Step 2: Match layers
    print("\nMatching layers...")
    matched, only_a, only_b = match_layers(layers_a, layers_b, verbose=args.verbose)
    print(f"  {len(matched)} pairs matched, "
          f"{len(only_a)} only-in-A, {len(only_b)} only-in-B")

    # Step 3: Per-pair diff
    print("Computing per-pair diffs...")
    pair_results = []
    for a, b, method in matched:
        pr = diff_pair(a, b, method)
        pair_results.append(pr)

    n_id = sum(1 for p in pair_results if p.status == "IDENTICAL")
    n_ch = sum(1 for p in pair_results if p.status == "CHANGED")
    n_sk = sum(1 for p in pair_results if p.status == "LENGTH_MISMATCH")
    print(f"  {n_id} identical, {n_ch} changed, {n_sk} skipped (length mismatch)")

    # Step 4: Group and select representative
    if args.verbose:
        print("\nSelecting representatives...")
    group_results = group_and_select(pair_results, only_a, only_b, verbose=args.verbose)

    # Step 5: Terminal report
    output_terminal_report(group_results, args.file_a, args.file_b, verbose=args.verbose)

    # Step 6: Excel report (optional)
    if args.output:
        output_excel_report(group_results, args.file_a, args.file_b, args.output)


if __name__ == "__main__":
    main()
